"""
Question-answering (chatbot) engine for DocumentPreview.

Goal: answer the reviewer's questions about whether a technical parameter HAS
appeared in previous YCKT documents, with what value, and whether it can be used
as a reference when auditing a new document.

Grounded on 2 sources (RAG):
- SOURCE A — historical YCKT store (history retriever).
- SOURCE B — document under review (current retriever).

Citations are taken DIRECTLY from the metadata of the retrieved nodes, NOT
generated by the LLM — to avoid fabricated sources.

Layered design:
- The formatting helpers (format_context, build_messages, build_citations) are PURE,
  operating on node-like objects → testable without Ollama/LLM.
- Only answer_question() calls Settings.llm (the real LLM).
"""

