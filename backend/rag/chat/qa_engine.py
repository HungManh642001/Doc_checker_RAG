# ---------------------------------------------------------------------------
# Node accessors — support both NodeWithScore (llama_index) and fake objects in tests
# ---------------------------------------------------------------------------

def _get_node(nw):
    """Return the TextNode inside a NodeWithScore (or itself if already a node)."""
    return getattr(nw, "node", nw)


def _node_text(nw) -> str:
    node = _get_node(nw)
    if hasattr(node, "get_content"):
        try:
            return node.get_content() or ""
        except Exception:  # noqa: BLE001
            pass
    return getattr(node, "text", "") or ""


def _node_metadata(nw) -> Dict:
    return getattr(_get_node(nw), "metadata", {}) or {}


def _node_score(nw) -> Optional[float]:
    score = getattr(nw, "score", None)
    try:
        return float(score) if score is not None else None
    except (TypeError, ValueError):
        return None


# ---------------------------------------------------------------------------
# Pure helpers (testable)
# ---------------------------------------------------------------------------

def format_context(nodes: List, label: str) -> str:
    """Join the content of the retrieved nodes into one source-labeled context block."""
    if not nodes:
        return f"[{label}]\n(Không có dữ liệu liên quan.)"
    parts = [f"[{label}]"]
    for i, nw in enumerate(nodes, 1):
        parts.append(f"{i}. {_node_text(nw)}")
    return "\n".join(parts)


def build_citations(nodes_a: List, nodes_b: List) -> List[Dict]:
    """
    Build the citation list from node metadata (deduped by doc + section + parameter).

    Each citation: {source, doc_name, section, param_name, param_value, score}.
    """
    citations: List[Dict] = []
    seen = set()
    for source, nodes in (
        ("YCKT trước đây", nodes_a or []),
        ("Tài liệu đang xét", nodes_b or []),
    ):
        for nw in nodes:
            md = _node_metadata(nw)
            key = (
                source,
                md.get("doc_name", ""),
                md.get("section", ""),
                md.get("param_name", ""),
            )
            if key in seen:
                continue
            seen.add(key)
            citations.append({
                "source": source,
                "doc_name": md.get("doc_name", ""),
                "section": md.get("section", ""),
                "param_name": md.get("param_name", ""),
                "param_value": md.get("param_value", ""),
                "score": _node_score(nw),
            })
    return citations


def build_messages(
    question: str,
    ctx_a: str,
    ctx_b: str,
    history: Optional[List[Dict]] = None,
    focus_param: Optional[str] = None,
    include_current: bool = True,
) -> List[ChatMessage]:
    """
    Build the ChatMessage list: system + conversation history + question with context.

    history: list of {"role": "user"|"assistant", "content": str}.
    focus_param: the parameter/device the user is interested in.
    include_current: if False, do NOT include SOURCE B (the document under review) in
                     the context — only look up previous YCKT documents (SOURCE A) as
                     the comparison basis.
    """
    messages: List[ChatMessage] = [
        ChatMessage(role=MessageRole.SYSTEM, content=SYSTEM_PROMPT)
    ]

    for turn in history or []:
        role = (turn.get("role") or "").lower()
        content = turn.get("content") or ""
        if not content:
            continue
        messages.append(ChatMessage(
            role=MessageRole.USER if role == "user" else MessageRole.ASSISTANT,
            content=content,
        ))

    focus = (
        f"\nĐối tượng đang quan tâm (thiết bị/vật liệu hoặc thông số): {focus_param}"
        if focus_param else ""
    )
    if include_current:
        sources_block = f"{ctx_a}\n\n{ctx_b}\n\n"
    else:
        sources_block = (
            f"{ctx_a}\n\n"
            "(CHỈ tra cứu các YCKT TRƯỚC ĐÂY — KHÔNG dùng tài liệu đang thẩm định. "
            "Chỉ nêu thông tin từ các tài liệu trước đây để làm cơ sở đối chiếu.)\n\n"
        )

    user_content = (
        f"{sources_block}"
        f"--------------------\n"
        f"CÂU HỎI: {question}{focus}"
    )
    messages.append(ChatMessage(role=MessageRole.USER, content=user_content))
    return messages


def _safe_retrieve(retriever, query: str) -> List:
    """Safe retrieval — return [] if retriever is None or errors out (don't block chat)."""
    if retriever is None:
        return []
    try:
        return retriever.retrieve(query)
    except Exception as e:  # noqa: BLE001
        print(f"[Chat] Lỗi retrieve: {e}")
        return []


# ---------------------------------------------------------------------------
# Main function — calls the real LLM
# ---------------------------------------------------------------------------

def answer_question(
    question: str,
    history_retriever=None,
    current_retriever=None,
    history: Optional[List[Dict]] = None,
    focus_param: Optional[str] = None,
    include_current: bool = True,
    top_k: int = 6,
) -> Dict:
    """
    Answer one of the reviewer's questions using RAG.

    include_current: if False, ONLY look up the previous YCKT store (SOURCE A) — do not
                     pull information from the document under review (SOURCE B). Used when
                     the user wants to see device information from older documents as a
                     comparison basis, without mixing in the content under review.

    Returns:
        {"answer": <answer string>, "citations": [<citation>...]}
    """
    query = f"{question} {focus_param}".strip() if focus_param else question

    nodes_a = _safe_retrieve(history_retriever, query)
    nodes_b = _safe_retrieve(current_retriever, query) if include_current else []

    ctx_a = format_context(nodes_a, "NGUỒN A — YCKT trước đây")
    ctx_b = format_context(nodes_b, "NGUỒN B — Tài liệu đang xét")
    messages = build_messages(
        question, ctx_a, ctx_b, history, focus_param, include_current=include_current
    )

    # Lazy import: side-effect of configuring Settings.llm (per LLM_PROVIDER).
    from llama_index.core import Settings
    import rag.audit_logic.audit_models  # noqa: F401  (ensure Settings.llm is ready)

    response = Settings.llm.chat(messages)
    answer_text = (getattr(response.message, "content", "") or "").strip()

    return {
        "answer": answer_text,
        "citations": build_citations(nodes_a, nodes_b),
    }
